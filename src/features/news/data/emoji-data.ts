export function getAllEmojis(): string[] {
  return EMOJI_CATEGORIES.flatMap((category) => category.emojis);
}

export function chunkEmojis(emojis: string[], size = 8): string[][] {
  const rows: string[][] = [];
  for (let index = 0; index < emojis.length; index += size) {
    rows.push(emojis.slice(index, index + size));
  }
  return rows;
}

export function filterEmojiCategories(query: string): EmojiCategory[] {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return EMOJI_CATEGORIES;

  return EMOJI_CATEGORIES.filter(
    (category) =>
      category.title.toLowerCase().includes(normalized) ||
      category.id.toLowerCase().includes(normalized),
  );
}
